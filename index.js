//6. PATCH a joke
//call localhost:3001/updateJoke?id=
// and then then in body have "text" for joke and "type" for joke Type 
app.patch("/updateJoke", (req, res) => {
  const id = parseInt(req.query.id);
  const existingJoke = jokes.find((joke) => joke.id === id);
  const replacementJoke = {
    id: id,
    jokeText: req.body.text || existingJoke.jokeText,
    jokeType: req.body.type || existingJoke.jokeType,
  };
  const searchIndex = jokes.findIndex((joke) => joke.id === id);
  jokes[searchIndex] = replacementJoke;
  console.log(jokes[searchIndex]);
  res.json(replacementJoke);
});

//7. DELETE Specific joke
//call localhost:3001/delete?id=
app.delete('/delete', (req,res)=>{
  const id = parseInt(req.query.id);
  const Jokeindex = jokes.findIndex((joke)=>joke.id === id);
  if(Jokeindex > -1){
    jokes.splice(Jokeindex, 1);
    res.sendStatus(200);
  }else{
    res.status(400) .json({id: id, error:`joke with id ${id} not found.`});
  }
});

